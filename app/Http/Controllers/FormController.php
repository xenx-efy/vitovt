    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $userData = new UserData();
        $userData->city = $request->city;
        $userData->school = $request->school;
        $userData->lastName = $request->lastName;
        $userData->name = $request->name;
        $userData->save();

        $maxPosOnSite = cookie('posOnSite', 1, 300);
        $cookieLastName = cookie('lastName', $request->lastName, 100);
        $cookieName = cookie('name', $request->name, 100);

        return response()->view('mapOfBelarus')
            ->cookie($cookieName)
            ->cookie($cookieLastName)
            ->cookie($maxPosOnSite);

    }

